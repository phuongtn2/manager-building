package com.building.dto;


public class RoleGroupDto {

	private int roleGroupId;
	private String roleGroupName;
	private String roleGroupMemo;
	public int getRoleGroupId() {
		return roleGroupId;
	}
	public void setRoleGroupId(int roleGroupId) {
		this.roleGroupId = roleGroupId;
	}
	public String getRoleGroupName() {
		return roleGroupName;
	}
	public void setRoleGroupName(String roleGroupName) {
		this.roleGroupName = roleGroupName;
	}
	public String getRoleGroupMemo() {
		return roleGroupMemo;
	}
	public void setRoleGroupMemo(String roleGroupMemo) {
		this.roleGroupMemo = roleGroupMemo;
	}
}
